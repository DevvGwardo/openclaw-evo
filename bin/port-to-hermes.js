#!/usr/bin/env node
import { readFileSync, writeFileSync, mkdirSync, cpSync, rmSync, existsSync, readdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
function log(msg) { console.log(`[port-to-hermes] ${msg}`); }

function rep(file, pairs) {
  if (!existsSync(file)) { log(` absent: ${file}`); return; }
  let src = readFileSync(file, 'utf-8');
  let changed = false;
  for (const [from, to] of pairs) {
    if (src.includes(from)) { src = src.split(from).join(to); changed = true; }
  }
  if (changed) { writeFileSync(file, src, 'utf-8'); log(` patched: ${file}`); }
  else log(`skipped: ${file}`);
}

function rx(file, pairs) {
  if (!existsSync(file)) { log(` absent: ${file}`); return; }
  let src = readFileSync(file, 'utf-8');
  let changed = false;
  for (const [re, to] of pairs) {
    if (re.test(src)) { src = src.replace(re, to); changed = true; }
  }
  if (changed) { writeFileSync(file, src, 'utf-8'); log(` patched: ${file}`); }
  else log(`skipped: ${file}`);
}

// 1. Rename src/openclaw/ -> src/hermes/
const OC = join(ROOT, 'src', 'openclaw');
const HG = join(ROOT, 'src', 'hermes');
if (existsSync(OC)) {
  if (existsSync(HG)) rmSync(HG, { recursive: true, force: true });
  mkdirSync(HG, { recursive: true });
  cpSync(OC, HG, { recursive: true });
  rmSync(OC, { recursive: true, force: true });
  log('src/openclaw/ -> src/hermes/');
} else { log('src/openclaw/ already migrated'); }

// 2. gateway.ts
const gw = join(ROOT, 'src', 'hermes', 'gateway.ts');
if (existsSync(gw)) {
  let src = readFileSync(gw, 'utf-8');
  src = src.replace(
    /const configPath = join\(homedir\(\), ['"]\.openclaw['"], ['"]openclaw\.json['"]\);[\s\S]*?catch \{[\s\S]*?return '';[\s\S]*?\}/,
    `const configPath = join(homedir(), '.hermes', 'hermes-agent', 'config.json');
  try {
    const config = JSON.parse(readFileSync(configPath, 'utf-8'));
    return config?.token ?? config?.auth?.token ?? config?.gateway?.auth?.token ?? '';
  } catch {
    try {
      const authPath = join(homedir(), '.hermes', 'auth.json');
      const auth = JSON.parse(readFileSync(authPath, 'utf-8'));
      return auth?.token ?? '';
    } catch { return ''; }
  }`
  );
  rep(gw, [
    ['OpenClaw Evo — OpenClaw Gateway Client', 'Hermes Evo — Hermes Gateway Client'],
    ['OpenClaw Gateway', 'Hermes Gateway'],
    ["'evo-monitor'", "'hermes-evo-monitor'"],
    ["displayName: 'OpenClaw Evo Monitor'", "displayName: 'Hermes Evo Monitor'"],
    ["'openclaw', 'identity", "'.hermes', 'hermes-agent', 'identity"],
  ]);
  log('patched gateway.ts');
}

// 3. sessionManager.ts
rep(join(ROOT, 'src', 'hermes', 'sessionManager.ts'), [
  ['OpenClaw Evo — Session Manager', 'Hermes Evo — Session Manager'],
]);

// 4. skillManager.ts
rx(join(ROOT, 'src', 'hermes', 'skillManager.ts'), [
  [/constructor\(skillsDir = '~\/\.openclaw\/skills\/'\)/, "constructor(skillsDir = '~/.hermes/skills/')"],
  [/\.openclaw\/skills\//g, '.hermes/skills/'],
  [/Generated by OpenClaw Evo/g, 'Generated by Hermes Evo'],
]);

// 5. hub.ts, cycle.ts, server.ts, cli.ts, watchdog.ts
for (const f of ['hub.ts','cycle.ts','server.ts','cli.ts','watchdog.ts']) {
  const p = join(ROOT, 'src', f);
  if (!existsSync(p)) continue;
  rep(p, [
    ["'./openclaw/", "'./hermes/"],
    ["'../openclaw/", "'../hermes/"],
  ]);
}

// 5b. experiment/runner.ts & promoter.ts
for (const f of ['runner.ts','promoter.ts']) {
  const p = join(ROOT, 'src', 'experiment', f);
  if (!existsSync(p)) continue;
  rep(p, [["../openclaw/", "../hermes/"]]);
  rx(p, [
    [/\.openclaw\/evo-memory/g, '.hermes/evo-memory'],
    [/\.openclaw\/skills/g, '.hermes/skills'],
    [/OpenClaw picks it up on next startup/g, 'Hermes picks it up on next startup'],
  ]);
}

// 5c. memory/store.ts
rx(join(ROOT, 'src', 'memory', 'store.ts'), [
  [/\.openclaw\/evo-memory/g, '.hermes/evo-memory'],
]);

// 5d. experiment/experimentLog.ts
rx(join(ROOT, 'src', 'experiment', 'experimentLog.ts'), [
  [/\.openclaw\/evo-memory/g, '.hermes/evo-memory'],
]);

// 6. harness/monitor.ts
rx(join(ROOT, 'src', 'harness', 'monitor.ts'), [
  [/OPENCLAW_GATEWAY_TOKEN/g, 'HERMES_GATEWAY_TOKEN'],
  [/OPENCLAW_POLL_INTERVAL_MS/g, 'HERMES_POLL_INTERVAL_MS'],
  [/Harness Monitor/g, 'Hermes Harness Monitor'],
  [/OpenClaw event hooks/g, 'Hermes event hooks'],
  [/\.openclaw\/identity/g, '.hermes/hermes-agent/identity'],
]);

// 7. harness/sessionTracker.ts & toolAnalyzer.ts
for (const f of ['sessionTracker.ts','toolAnalyzer.ts']) {
  rep(join(ROOT, 'src', 'harness', f), [
    ['OpenClaw Evo —', 'Hermes Evo —'],
  ]);
}

// 8a. src/types.ts
rep(join(ROOT, "src", "types.ts"), [
  ["OPENCLAW_GATEWAY_URL", "HERMES_GATEWAY_URL"],
  ["OPENCLAW_POLL_INTERVAL_MS", "HERMES_POLL_INTERVAL_MS"],
]);

// 8. src/constants.ts
rx(join(ROOT, 'src', 'constants.ts'), [
  [/OPENCLAW_GATEWAY_URL.*/g,  "HERMES_GATEWAY_URL:         env('HERMES_GATEWAY_URL',         'http://localhost:18789'),"],
  [/OPENCLAW_POLL_INTERVAL_MS.*/g, "HERMES_POLL_INTERVAL_MS:    envInt('HERMES_POLL_INTERVAL_MS',   10000),"],
  [/SKILL_OUTPUT_DIR.*/g,       "SKILL_OUTPUT_DIR:           env('SKILL_OUTPUT_DIR',            '~/.hermes/skills/'),"],
  [/MEMORY_DIR.*/g,             "MEMORY_DIR:                 env('MEMORY_DIR',                  '~/.hermes/evo-memory/'),"],
]);

// 9. openclaw-skill/SKILL.md
rep(join(ROOT, 'openclaw-skill', 'SKILL.md'), [
  ['OpenClaw Evo', 'Hermes Evo'],
  ['openclaw-evo', 'hermes-evo'],
]);

// 10. Shell scripts
for (const s of ['evolve.sh','polish.sh']) {
  rep(join(ROOT, s), [
    ['.openclaw', '.hermes'],
    ['openclaw', 'hermes'],
  ]);
}

// 11. .env.example
rep(join(ROOT, '.env.example'), [
  ['OPENCLAW_GATEWAY_URL', 'HERMES_GATEWAY_URL'],
  ['OPENCLAW_POLL_INTERVAL_MS', 'HERMES_POLL_INTERVAL_MS'],
  ['.openclaw', '.hermes'],
]);

// 12. README.md
const rmFile = join(ROOT, 'README.md');
if (existsSync(rmFile)) {
  let src = readFileSync(rmFile, 'utf-8');
  src = src
    .replace(/OpenClaw Evo/g, 'Hermes Evo')
    .replace(/openclaw-evo/g, 'hermes-evo')
    .replace(/DevvGwardo\/openclaw-evo/g, 'DevvGwardo/hermes-evo')
    .replace(/self-evolution engine for \[OpenClaw\]/g, 'self-evolution engine for Hermes')
    .replace(/monitors, evaluates, and improves itself/g, 'monitors, evaluates, and improves Hermes');
  writeFileSync(rmFile, src, 'utf-8');
  log('patched README.md');
}

// 13. CI/CD workflows
for (const wf of ['ci.yml','evolution.yml','release.yml']) {
  rep(join(ROOT, '.github', 'workflows', wf), [
    ['openclaw-evo', 'hermes-evo'],
    ['OpenClaw Evo', 'Hermes Evo'],
  ]);
}

log('Port complete.');
