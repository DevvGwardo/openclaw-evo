describe('Hub checkpoint and resume', () => {
  it('persists hub state to MemoryStore and restores it on a new instance', async () => {
    // Use the same store dir for both save and restore (simulates real Hub lifecycle
    // where EvoHub saves a checkpoint then creates a new instance and calls resume())
    const store = makeTempStore();
    await store.init();

    const proposedSkills: GeneratedSkill[] = [
      {
        id: `skill-${randomUUID()}`,
        name: 'Read Permission Fix',
        description: 'Handles permission errors for read operations',
        triggerPhrases: ['read permission error'],
        implementation: '// skill implementation placeholder text here to meet length requirements',
        examples: [{ input: 'read /etc/shadow', expectedOutput: 'ok', explanation: 'ok' }],
        confidence: 0.75,
        status: 'proposed',
        generatedAt: new Date(),
        targetFailurePattern: 'fp-1',
      },
    ];

    const state: HubState = {
      cycleNumber: 5,
      completedCycles: [],
      proposedSkills,
      activeExperiments: [],
      lastCheckpoint: new Date(),
    };

    // Save checkpoint
    await store.save('hub-state', state);

    // Verify file was written
    const keys = await store.list();
    expect(keys).toContain('hub-state');

    // Restore on a new store instance using the same directory
    const restored: HubState | null = await store.load<HubState>('hub-state');

    expect(restored).not.toBeNull();
    expect(restored!.cycleNumber).toBe(5);
    expect(restored!.proposedSkills).toHaveLength(1);
    expect(restored!.proposedSkills[0].name).toBe('Read Permission Fix');
    expect(restored!.lastCheckpoint).toBeDefined();
  });

  it('overwrites an existing checkpoint on re-save', async () => {
    const store = makeTempStore();
    await store.init();

    const state1: HubState = { cycleNumber: 1, completedCycles: [], proposedSkills: [], activeExperiments: [], lastCheckpoint: new Date() };
    const state2: HubState = { cycleNumber: 2, completedCycles: [], proposedSkills: [], activeExperiments: [], lastCheckpoint: new Date() };

    await store.save('hub-state', state1);
    await store.save('hub-state', state2);

    const restored = await store.load<HubState>('hub-state');
    expect(restored!.cycleNumber).toBe(2);
  });

  it('estimateSize returns a non-negative number', async () => {
    const store = makeTempStore();
    await store.init();
    expect(store.estimateSize()).toBeGreaterThanOrEqual(0);
  });

  it('getMemoryDir returns the configured directory path', async () => {
    const store = makeTempStore();
    expect(store.getMemoryDir()).toContain('.test-memory');
  });
});

