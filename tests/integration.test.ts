// TEST GROUP 1 — Evolution pipeline
// ══════════════════════════════════════════════════════════════════════════════

describe('Evolution pipeline', () => {

  // ── 1A. scoreSessions ─────────────────────────────────────────────────────
  describe('scoreSessions', () => {
    it('returns a valid PerformanceScore for a batch of sessions', () => {
      const sessions: SessionMetrics[] = [
        makeSession({ success: true,  errorCount: 0, totalToolCalls: 3 }),
        makeSession({ success: true,  errorCount: 0, totalToolCalls: 4 }),
        makeSession({ success: false, errorCount: 1, totalToolCalls: 2 }),
      ];

      const score = scoreSessions(sessions);

      expect(score.overall).toBeGreaterThan(0);
      expect(score.overall).toBeLessThanOrEqual(100);
      expect(score.accuracy).toBeGreaterThan(0);
      // 2/3 sessions succeeded → accuracy ≈ 66.67
      expect(score.accuracy).toBeCloseTo(66.67, 0);
    });

    it('returns zero scores when no sessions are provided', () => {
      const score = scoreSessions([]);
      expect(score.overall).toBe(0);
      expect(score.accuracy).toBe(0);
    });

    it('awards max efficiency when tool calls equal the optimal count', () => {
      const sessions = [makeSession({ totalToolCalls: 3, success: true, errorCount: 0 })];
      const score = scoreSessions(sessions, 60_000, 3);  // optimalCalls = 3
      expect(score.efficiency).toBe(100);
    });
  });

  // ── 1B. detectPatterns ────────────────────────────────────────────────────
  describe('detectPatterns', () => {
    it('detects a repeating failure pattern across multiple sessions', () => {
      const failureCall = makeFailedToolCall({ name: 'read', error: 'Permission denied' });

      const sessions: SessionMetrics[] = [0, 1, 2].map((i) =>
        makeSession({
          sessionId: `s${i}`,
          toolCalls: [{ ...failureCall, id: `tc-${i}`, startTime: Date.now() + i * 1000 }],
          success: false,
          errorCount: 1,
          totalToolCalls: 1,
        }),
      );

      const patterns = detectPatterns(sessions);

      expect(patterns.length).toBeGreaterThan(0);
      const readPattern = patterns.find((p) => p.toolName === 'read');
      expect(readPattern).toBeDefined();
      expect(readPattern!.frequency).toBeGreaterThanOrEqual(3);
    });

    it('returns an empty array when no sessions contain failures', () => {
      const sessions = [makeSession({ success: true, errorCount: 0, toolCalls: [] })];
      expect(detectPatterns(sessions)).toHaveLength(0);
    });

    it('sorts patterns by frequency descending', () => {
      const highFreqCall = makeFailedToolCall({ name: 'http', error: 'Timeout' });
      const lowFreqCall  = makeFailedToolCall({ name: 'read', error: 'NotFound' });

      const sessions: SessionMetrics[] = [
        ...Array.from({ length: 5 }, (_, i) =>
          makeSession({
            sessionId: `high-${i}`,
            toolCalls: [{ ...highFreqCall, id: `h-${i}`, startTime: Date.now() + i }],
            success: false, errorCount: 1, totalToolCalls: 1,
          }),
        ),
        ...Array.from({ length: 2 }, (_, i) =>
          makeSession({
            sessionId: `low-${i}`,
            toolCalls: [{ ...lowFreqCall, id: `l-${i}`, startTime: Date.now() + 100 + i }],
            success: false, errorCount: 1, totalToolCalls: 1,
          }),
        ),
      ];

      const patterns = detectPatterns(sessions);
      expect(patterns[0].frequency).toBeGreaterThanOrEqual(patterns[1].frequency);
    });
  });

  // ── 1C. generateFromFailure ─────────────────────────────────────────────
  describe('generateFromFailure', () => {
    it('generates a valid skill from a failure pattern with high confidence', () => {
      const pattern: FailurePattern = {
        id: `fp-${randomUUID()}`,
        toolName: 'read',
        errorType: 'permission_error',
        errorMessage: 'Permission denied',
        frequency: 5,
        severity: 'high',
        exampleContexts: [
          {
            sessionId: 's1',
            taskDescription: 'Read /etc/hosts',
            toolInput: { path: '/etc/hosts' },
            errorOutput: 'Permission denied',
            timestamp: new Date(),
          },
        ],
        firstSeen: new Date(Date.now() - 86_400_000),
        lastSeen: new Date(),
        autoFixAvailable: false,
      };

      const result = generateFromFailure(pattern, { skipValidation: false });

      expect(result.skill).toBeDefined();
      expect(result.skill.name).toContain('read');
      expect(result.skill.status).toBe('proposed');
      expect(result.skill.confidence).toBeGreaterThan(0);
      expect(result.skill.implementation.length).toBeGreaterThan(100);
      expect(result.skill.triggerPhrases.length).toBeGreaterThan(0);
      expect(result.skill.targetFailurePattern).toBe(pattern.id);
    });

    it('generates a skill with skipValidation even when examples are missing', () => {
      const pattern: FailurePattern = {
        id: `fp-${randomUUID()}`,
        toolName: 'search',
        errorType: 'timeout',
        errorMessage: 'Request timed out',
        frequency: 1,
        severity: 'medium',
        exampleContexts: [],
        firstSeen: new Date(),
        lastSeen: new Date(),
        autoFixAvailable: false,
      };

      // Should not throw — validation is bypassed
      const result = generateFromFailure(pattern, { skipValidation: true });
      expect(result.skill.id).toBeTruthy();
    });

    it('throws when skipValidation is false and pattern produces a low-quality skill', () => {
      const pattern: FailurePattern = {
        id: `fp-${randomUUID()}`,
        toolName: 'unknown_tool',
        errorType: 'unknown',
        errorMessage: 'Something went wrong',
        frequency: 1,
        severity: 'low',
        exampleContexts: [],
        firstSeen: new Date(),
        lastSeen: new Date(),
        autoFixAvailable: false,
      };

      // Low frequency + no examples → low confidence + short implementation → validation fails
      expect(() => generateFromFailure(pattern)).toThrow();
    });

    it('generates distinct skill names for distinct failure patterns', () => {
      const patterns: FailurePattern[] = [
        {
          id: `fp-${randomUUID()}`, toolName: 'read', errorType: 'not_found',
          errorMessage: 'File does not exist', frequency: 3, severity: 'medium',
          exampleContexts: [], firstSeen: new Date(), lastSeen: new Date(),
          autoFixAvailable: false, suggestedFix: 'Check the file path before reading.',
        },
        {
          id: `fp-${randomUUID()}`, toolName: 'write', errorType: 'permission_error',
          errorMessage: 'Permission denied', frequency: 3, severity: 'high',
          exampleContexts: [], firstSeen: new Date(), lastSeen: new Date(),
          autoFixAvailable: false, suggestedFix: 'Grant write permissions to the target directory.',
        },
      ];

      const results = patterns.map((p) => generateFromFailure(p, { skipValidation: true }));
      const names = results.map((r) => r.skill.name);
      expect(new Set(names).size).toBe(names.length);
    });
  });

  // ── 1D. validate ──────────────────────────────────────────────────────────
  describe('validate', () => {
    it('returns valid=true for a well-formed skill from generateFromFailure', () => {
      const pattern: FailurePattern = {
        id: `fp-${randomUUID()}`,
        toolName: 'http',
        errorType: 'timeout',
        errorMessage: 'Request timed out',
        frequency: 5,
        severity: 'high',
        exampleContexts: [
          {
            sessionId: 's1',
            taskDescription: 'Fetch API data',
            toolInput: { url: 'https://example.com' },
            errorOutput: 'Timeout',
            timestamp: new Date(),
          },
        ],
        firstSeen: new Date(),
        lastSeen: new Date(),
        autoFixAvailable: false,
      };

      const { skill } = generateFromFailure(pattern);
      const result = validate(skill);

      expect(result.valid).toBe(true);
      expect(result.errors).toHaveLength(0);
    });

    it('returns errors for a skill missing a name', () => {
      const skill: GeneratedSkill = {
        id: randomUUID(),
        name: '',
        description: 'A test skill with a sufficiently long description',
        triggerPhrases: ['test phrase'],
        implementation: '// some code here that is definitely more than 100 chars to pass length checks.',
        examples: [{ input: 'test', expectedOutput: 'ok', explanation: 'test' }],
        confidence: 0.8,
        status: 'proposed',
        generatedAt: new Date(),
      };

      const result = validate(skill);
      expect(result.valid).toBe(false);
      expect(result.errors.some((e) => e.toLowerCase().includes('name'))).toBe(true);
    });

    it('returns errors for a skill with no trigger phrases', () => {
      const skill: GeneratedSkill = {
        id: randomUUID(),
        name: 'Test Skill',
        description: 'A test skill description that is long enough',
        triggerPhrases: [],
        implementation: '// some code here that is definitely more than 100 chars. this is enough content.',
        examples: [{ input: 'test', expectedOutput: 'ok', explanation: 'test' }],
        confidence: 0.5,
        status: 'proposed',
        generatedAt: new Date(),
      };

      const result = validate(skill);
      expect(result.valid).toBe(false);
      expect(result.errors.some((e) => e.toLowerCase().includes('trigger'))).toBe(true);
    });

    it('returns errors for a skill with unfilled template placeholders', () => {
      const skill: GeneratedSkill = {
        id: randomUUID(),
        name: 'Bad Skill',
        description: 'Has unfilled template markers',
        triggerPhrases: ['test trigger'],
        implementation: '// TODO: fill in {{TOOL_NAME}} and {{ERROR_TYPE}} placeholders',
        examples: [{ input: 'test', expectedOutput: 'ok', explanation: 'test' }],
        confidence: 0.5,
        status: 'proposed',
        generatedAt: new Date(),
      };

      const result = validate(skill);
      expect(result.valid).toBe(false);
      expect(result.errors.some((e) => e.includes('{{TOOL_NAME}}'))).toBe(true);
    });

    it('returns warnings for a skill with very short description', () => {
      const skill: GeneratedSkill = {
        id: randomUUID(),
        name: 'Short Desc Skill',
        description: 'Too short',
        triggerPhrases: ['test trigger'],
        implementation: '// some code here that is definitely more than 100 chars. this is enough content.',
        examples: [{ input: 'test', expectedOutput: 'ok', explanation: 'test' }],
        confidence: 0.5,
        status: 'proposed',
        generatedAt: new Date(),
      };

      const result = validate(skill);
      expect(result.warnings.some((w) => w.toLowerCase().includes('short'))).toBe(true);
    });
  });

  // ── 1E. promoter.evaluate ─────────────────────────────────────────────────
  describe('promoter.evaluate', () => {
    it('recommends promotion when treatment significantly outperforms control', () => {
      const skillId = `skill-${randomUUID()}`;
      const experiment = makeWinningExperiment(skillId);

      promoter.register(experiment);
      const decision = promoter.evaluate(experiment);

      // 6/10 vs 9/10 should produce statistically significant improvement
      expect(decision.promoted).toBe(true);
      expect(decision.experimentsValidated).toBeGreaterThan(0);
    });

    it('rejects promotion when improvement is below threshold', () => {
      // Both arms have identical results → 0% improvement
      const makeResults = (successes: number): ExperimentResult[] =>
        Array.from({ length: 10 }, (_, i) => ({
          taskId: `task-${i}`,
          success: i < successes,
          toolCalls: 3,
          durationMs: 5000,
          score: i < successes ? 80 : 0,
        }));

      const experiment: Experiment = {
        id: `exp-${randomUUID()}`,
        name: 'No-Gain Experiment',
        description: 'No improvement over baseline',
        treatmentSkillId: `skill-${randomUUID()}`,
        controlSkillId: 'baseline',
        taskSet: [],
        status: 'completed',
        controlResults: makeResults(5),
        treatmentResults: makeResults(5),  // identical → 0% improvement
        statisticalSignificance: 0,
        improvementPct: 0,
        startedAt: new Date(),
        completedAt: new Date(),
      };

      promoter.register(experiment);
      const decision = promoter.evaluate(experiment);

      expect(decision.promoted).toBe(false);
      expect(decision.reason.toLowerCase()).toMatch(/improvement|threshold|not met/);
    });

    it('rejects a pending experiment immediately', () => {
      const experiment: Experiment = {
        id: `exp-${randomUUID()}`,
        name: 'Pending experiment',
        description: 'Not started yet',
        treatmentSkillId: `skill-${randomUUID()}`,
        taskSet: [],
        status: 'pending',
        controlResults: [],
        treatmentResults: [],
        statisticalSignificance: 0,
        improvementPct: 0,
        startedAt: new Date(),
      };

      promoter.register(experiment);
      const decision = promoter.evaluate(experiment);

      expect(decision.promoted).toBe(false);
      expect(decision.reason.toLowerCase()).toMatch(/pending|running|status/);
    });

    it('returns already-promoted for an experiment with promotedAt set', () => {
      const experiment: Experiment = {
        id: `exp-${randomUUID()}`,
        name: 'Already promoted',
        description: 'Was promoted previously',
        treatmentSkillId: `skill-${randomUUID()}`,
        taskSet: [],
        status: 'completed',
        controlResults: [],
        treatmentResults: [],
        statisticalSignificance: 0.99,
        improvementPct: 20,
        startedAt: new Date(),
        completedAt: new Date(),
        promotedAt: new Date(),
      };

      promoter.register(experiment);
      const decision = promoter.evaluate(experiment);

      expect(decision.promoted).toBe(true);
      expect(decision.reason.toLowerCase()).toMatch(/already|previous/);
    });

    it('returns error decision for an unknown experiment ID', () => {
      const decision = promoter.evaluate('nonexistent-experiment-id');
      expect(decision.promoted).toBe(false);
      expect(decision.reason.toLowerCase()).toMatch(/not found/i);
    });

    it('isSignificant helper returns true above threshold', () => {
      const skillId = `skill-${randomUUID()}`;
      const experiment = makeWinningExperiment(skillId);
      promoter.register(experiment);

      const statResult = comparator.compare(experiment);
      expect(comparator.isSignificant(statResult, 0.95)).toBe(true);
    });
  });
});

