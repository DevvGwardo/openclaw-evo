function makeFailedToolCall(overrides: Partial<import('../src/types.js').ToolCall> = {}): import('../src/types.js').ToolCall {
  return {
    id: randomUUID(),
    name: 'read',
    input: { path: '/etc/hosts' },
    output: undefined,
    error: 'Permission denied: /etc/hosts',
    startTime: Date.now() - 1000,
    endTime: Date.now(),
    success: false,
    ...overrides,
  };
}

function makeSession(overrides: Partial<SessionMetrics> = {}): SessionMetrics {
  return {
    sessionId: randomUUID(),
    toolCalls: [],
    startTime: Date.now() - 60_000,
    endTime: Date.now(),
    success: false,
    errorCount: 1,
    totalToolCalls: 1,
    avgLatencyMs: 500,
    taskType: 'file_read',
    ...overrides,
  };
}

/** Build an experiment with explicit result arrays that pass 95% confidence threshold. */
function makeWinningExperiment(skillId: string): Experiment {
  // 0/20 control vs 20/20 treatment → 100% improvement, z=5.16, p≈1.2e-7, confidence≈100%
  const controlResults: ExperimentResult[] = Array.from({ length: 20 }, (_, i) => ({
    taskId: `task-${i}`,
    success: false,  // 0/20 = 0%
    toolCalls: 3,
    durationMs: 5000,
    score: 0,
  }));

  const treatmentResults: ExperimentResult[] = Array.from({ length: 20 }, (_, i) => ({
    taskId: `task-${i}`,
    success: true,   // 20/20 = 100%
    toolCalls: 2,
    durationMs: 4000,
    score: 100,
  }));

  return {
    id: `exp-${randomUUID()}`,
    name: 'A/B: Test Skill',
    description: 'Integration test experiment',
    treatmentSkillId: skillId,
    controlSkillId: 'baseline',
    taskSet: [],
    status: 'completed',
    controlResults,
    treatmentResults,
    statisticalSignificance: 0,
    improvementPct: 0,
    startedAt: new Date(Date.now() - 60_000),
    completedAt: new Date(),
  };
}

