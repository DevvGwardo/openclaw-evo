// TEST GROUP 4 — Approval workflow: promote → approveSkill → deployed
// ══════════════════════════════════════════════════════════════════════════════

describe('Approval workflow', () => {
  it('promote() returns requires_approval with an approvalId without deploying', async () => {
    const skillId = `skill-${randomUUID()}`;
    const experiment = makeWinningExperiment(skillId);

    promoter.register(experiment);

    const result = await promoter.promote(experiment.id);

    expect(result.promoted).toBe(false);
    expect(result.reason).toBe('requires_approval');
    expect(result.approvalId).toBeDefined();
    expect(result.approvalId).toMatch(/^approval-/);

    // Skill should NOT be written to the skills dir yet
    const skillFile = join(TEST_SKILLS_DIR, skillId, 'SKILL.md');
    await expect(readFile(skillFile, 'utf-8')).rejects.toThrow();
  });

  it('getPendingApprovals() returns skills awaiting approval', async () => {
    const skillId = `skill-${randomUUID()}`;
    const experiment = makeWinningExperiment(skillId);

    promoter.register(experiment);
    await promoter.promote(experiment.id);

    const pending = promoter.getPendingApprovals();
    expect(pending.some((a) => a.skillId === skillId && a.status === 'pending')).toBe(true);
  });

  it('approveSkill() deploys the skill and updates approval status to approved', async () => {
    const skillId = `skill-${randomUUID()}`;
    const experiment = makeWinningExperiment(skillId);

    promoter.register(experiment);
    const { approvalId } = await promoter.promote(experiment.id);
    expect(approvalId).toBeDefined();

    await promoter.approveSkill(approvalId!);

    // Approval should no longer be in the pending list
    const pending = promoter.getPendingApprovals();
    expect(pending.find((a) => a.skillId === skillId && a.status === 'pending')).toBeUndefined();

    // Skill file should be written to the mocked skills dir
    const skillFile = join(TEST_SKILLS_DIR, skillId, 'SKILL.md');
    const content = await readFile(skillFile, 'utf-8');
    expect(content).toContain(experiment.name.replace('A/B: ', ''));
  });

  it('approveSkill() throws for a non-existent approvalId', async () => {
    await expect(promoter.approveSkill('this-approval-does-not-exist')).rejects.toThrow(
      /approval not found/i,
    );
  });

  it('approveSkill() throws when the approval is already approved', async () => {
    const skillId = `skill-${randomUUID()}`;
    const experiment = makeWinningExperiment(skillId);

    promoter.register(experiment);
    const { approvalId } = await promoter.promote(experiment.id);

    // First approve — succeeds
    await promoter.approveSkill(approvalId!);

    // Second approve — throws
    await expect(promoter.approveSkill(approvalId!)).rejects.toThrow(/already/i);
  });

  it('approveSkill() deploys the skill and updates approval status to approved', async () => {
    // Use a winning experiment (0/20 vs 20/20 → 100% confidence)
    const skillId = `skill-${randomUUID()}`;
    const experiment = makeWinningExperiment(skillId);
    promoter.register(experiment);

    // promote() creates a pending approval (requires_approval flow)
    const { approvalId } = await promoter.promote(experiment.id);
    expect(approvalId).toBeDefined();
    expect(typeof approvalId).toBe('string');

    // Approve it — should not throw
    await promoter.approveSkill(approvalId!);

    // Skill file should be written to the mocked skills dir
    const skillFile = join(TEST_SKILLS_DIR, skillId, 'SKILL.md');
    const content = await readFile(skillFile, 'utf-8');
    expect(content).toContain(experiment.name.replace('A/B: ', ''));
  });

  it('rejectSkill() throws when approval is not found', () => {
    expect(() => promoter.rejectSkill('nonexistent-approval-id')).toThrow(/approval not found/i);
  });

  it('promote() throws for an unknown experiment ID', async () => {
    await expect(promoter.promote('unknown-experiment-id')).rejects.toThrow(
      /experiment not found/i,
    );
  });

  it('promote() is idempotent — returns already promoted without writing files twice', async () => {
    const skillId = `skill-${randomUUID()}`;
    const experiment: Experiment = {
      id: `exp-${randomUUID()}`,
      name: 'Idempotent Test',
      description: 'Testing idempotency',
      treatmentSkillId: skillId,
      taskSet: [],
      status: 'completed',
      controlResults: [],
      treatmentResults: [],
      statisticalSignificance: 0.99,
      improvementPct: 20,
      startedAt: new Date(),
      completedAt: new Date(),
      promotedAt: new Date(),  // Already marked as promoted
    };

    promoter.register(experiment);

    const result = await promoter.promote(experiment.id);

    expect(result.promoted).toBe(true);
    expect(result.reason.toLowerCase()).toMatch(/already/i);
  });
});
